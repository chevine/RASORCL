INSERT INTO RAS.RAS_HOUSEHOLD_UNIT(
    TAX_YEAR, TAXID, PROPERTY_ID, TENANT_TAXID, RELATIONSHIP_ID, TAX_DEPENDANT
)
SELECT pwt.TAX_YEAR
, pwt.TAXID
, pwt.PROPERTY_ID
, pwt.TAXID AS TENANT_TAXID
, rr.RELATIONSHIP_ID
, 'N'
FROM RAS.PROPERTIES_WITH_TENANTS pwt
LEFT JOIN RAS.RAS_HOUSEHOLD_UNIT rhu ON (rhu.TAX_YEAR = pwt.TAX_YEAR AND rhu.TAXID = pwt.TAXID)
JOIN RAS.RAS_RELATIONSHIP rr ON (rr.RELATIONSHIP = 'SELF')
WHERE rhu.TAXID IS NULL
FETCH NEXT 100000 ROWS ONLY;
